    private void fillCombo() throws ClassNotFoundException, SQLException{
        ObjectControl control = new ObjectControl();
        ResultSet rs = control.getAAfromDB();
        while(rs.next()){
            String name = rs.getString("fName") + " " + rs.getString("sName");
            cmbAA.addItem(name);
        }
    }
